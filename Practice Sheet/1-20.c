#include <stdio.h>
int main()
{
    int d,m,y;
    printf("Enter Date (DD-MM-YYYY) = ");
    scanf("%d-%d-%d",&d,&m,&y);
    printf(" day = %d \n",d);
    printf(" month  = %d\n  ",m);
    printf(" year = %d \n ",y);
    return 0;
}



